package Inicio.EstruturasCondicionais;

public class Main {
    public static void main(String[] args) {
        int numero = 7;
        if(numero > 0) {
            System.out.println("Número posítivo.");
        } else if (numero < 0) {
            System.out.println("Número é negativo.");
        } else {
            System.out.println("Número é zero.");
        }
    }
}
