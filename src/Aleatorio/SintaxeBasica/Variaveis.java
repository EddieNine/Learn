package Aleatorio.SintaxeBasica;

public class Variaveis {
    public static void main(String[] args) {
        int idade = 25; // tipo inteiro
        float salario = 3500.50f; // tipo float (usa o "f" para indicar que é float)
        double preco  = 9.99; // tipo double (números de ponto flutuante mais precisos)
        boolean isAtivo = true; // tipo booleano (verdadeiro ou falso)
        String nome = "João"; // tipo String (sequência de caracteres)

    }
}
